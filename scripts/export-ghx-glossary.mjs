#!/usr/bin/env node
// Mirrors the GHX glossary (src/app/ghx/glossary.json — the source of truth
// behind zackproser.com/ghx) into the ghx-workshop repo as markdown + PDF.
//
// Usage:
//   npm run ghx:glossary-export                 # writes to ../ghx-workshop/repo/glossary
//   npm run ghx:glossary-export -- /some/dir    # custom output dir
//
// PDF rendering uses headless Google Chrome (--print-to-pdf).

import fs from 'fs'
import path from 'path'
import { execFileSync } from 'child_process'
import { fileURLToPath, pathToFileURL } from 'url'
import https from 'https'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const glossaryPath = path.join(__dirname, '../src/app/ghx/glossary.json')
const outDir = path.resolve(
  process.argv[2] || path.join(__dirname, '../../ghx-workshop/repo/glossary'),
)

function findChrome() {
  if (process.env.CHROME_PATH) return process.env.CHROME_PATH
  const platform = process.platform
  const candidates =
    platform === 'darwin'
      ? ['/Applications/Google Chrome.app/Contents/MacOS/Google Chrome']
      : platform === 'win32'
        ? [
            'C:\\Program Files\\Google\\Chrome\\Application\\chrome.exe',
            'C:\\Program Files (x86)\\Google\\Chrome\\Application\\chrome.exe',
          ]
        : ['/usr/bin/google-chrome', '/usr/bin/chromium-browser', '/usr/bin/chromium']
  for (const c of candidates) {
    if (fs.existsSync(c)) return c
  }
  return 'chrome'
}

function fetchQRCode(url, outputPath) {
  return new Promise((resolve, reject) => {
    const qrApiUrl = `https://api.qrserver.com/v1/create-qr-code/?size=1024x1024&data=${encodeURIComponent(url)}`
    https
      .get(qrApiUrl, (res) => {
        if (res.statusCode !== 200) {
          reject(new Error(`QR API returned ${res.statusCode}`))
          return
        }
        const chunks = []
        res.on('data', (chunk) => chunks.push(chunk))
        res.on('end', () => {
          fs.writeFileSync(outputPath, Buffer.concat(chunks))
          resolve()
        })
      })
      .on('error', reject)
  })
}

const CHROME = findChrome()
const LIVE_URL = 'https://zackproser.com/ghx'

const g = JSON.parse(fs.readFileSync(glossaryPath, 'utf8'))
fs.mkdirSync(outDir, { recursive: true })

// ── Markdown ────────────────────────────────────────────────────────────────
let md = `# ${g.title}\n\n`
md += `> ${g.subtitle}\n>\n> ${g.event} · Live version (with interactive demos): <${LIVE_URL}> · Updated ${g.updated}\n\n`
md += `Generated from \`zackproser.com/ghx\` — edit \`src/app/ghx/glossary.json\` in the portfolio repo, then run \`npm run ghx:glossary-export\`. Do not edit this file by hand.\n\n`
for (const s of g.sections) {
  md += `## ${s.level} — ${s.name}\n\n${s.intro}\n\n`
  for (const t of s.terms) {
    md += `### ${t.term}\n\n${t.definition}\n\n`
    if (t.hear) md += `*You'll hear:* ${t.hear}\n\n`
    if (t.demo) md += `*Try it live:* [${t.demo.label}](https://zackproser.com${t.demo.href})\n\n`
  }
}
fs.writeFileSync(path.join(outDir, 'glossary.md'), md.trimEnd() + '\n')

// ── Print HTML → PDF ────────────────────────────────────────────────────────
const esc = (s) =>
  s.replaceAll('&', '&amp;').replaceAll('<', '&lt;').replaceAll('>', '&gt;')

const sectionsHtml = g.sections
  .map(
    (s) => `
  <section>
    <div class="level-tag">${esc(s.level)}</div>
    <h2>${esc(s.name)}</h2>
    <p class="intro">${esc(s.intro)}</p>
    ${s.terms
      .map(
        (t) => `
    <div class="term">
      <h3>${esc(t.term)}</h3>
      <p>${esc(t.definition)}</p>
      ${t.hear ? `<p class="hear">You'll hear: ${esc(t.hear)}</p>` : ''}
      ${
        t.demo
          ? `<p class="demo">Try it live: <a href="https://zackproser.com${t.demo.href}">zackproser.com${t.demo.href}</a></p>`
          : ''
      }
    </div>`,
      )
      .join('')}
  </section>`,
  )
  .join('')

const html = `<!doctype html>
<html lang="en">
<head>
<meta charset="utf-8" />
<title>${esc(g.title)}</title>
<style>
  :root { --navy: #262B6B; --orange: #F16025; --ink: #1c1c28; --muted: #5b5b6e; }
  * { box-sizing: border-box; }
  body {
    font-family: -apple-system, "Helvetica Neue", Helvetica, Arial, sans-serif;
    color: var(--ink); margin: 0; font-size: 10.5pt; line-height: 1.5;
  }
  @page { margin: 16mm 14mm; }
  .masthead {
    background: var(--navy); color: #fff; padding: 22pt 24pt;
    border-radius: 8pt; display: flex; justify-content: space-between; gap: 16pt;
    -webkit-print-color-adjust: exact; print-color-adjust: exact;
  }
  .masthead .event {
    color: #F89B6C; font-size: 7.5pt; font-weight: 700;
    letter-spacing: 0.18em; text-transform: uppercase; margin: 0 0 6pt;
  }
  .masthead h1 { font-size: 24pt; margin: 0 0 6pt; letter-spacing: -0.02em; }
  .masthead .sub { color: #d5d9f5; margin: 0; font-size: 10pt; max-width: 360pt; }
  .qr { text-align: center; flex-shrink: 0; }
  .qr img { width: 86pt; height: 86pt; background: #fff; border-radius: 6pt; padding: 4pt; }
  .qr p { color: #d5d9f5; font-size: 7pt; margin: 4pt 0 0; }
  .promise { margin: 10pt 2pt 0; color: var(--muted); font-size: 9.5pt; }
  section { margin-top: 18pt; }
  .level-tag {
    color: var(--orange); font-weight: 800; font-size: 8pt;
    letter-spacing: 0.18em; text-transform: uppercase;
  }
  h2 {
    color: var(--navy); font-size: 16pt; margin: 2pt 0 4pt;
    border-bottom: 2pt solid var(--orange); padding-bottom: 4pt;
  }
  .intro { color: var(--muted); margin: 4pt 0 10pt; }
  .term { break-inside: avoid; margin-bottom: 9pt; }
  .term h3 { color: var(--navy); font-size: 11.5pt; margin: 0 0 2pt; }
  .term p { margin: 0; }
  .hear { color: var(--muted); font-style: italic; font-size: 9.5pt; margin-top: 2pt !important; }
  .demo { font-size: 9.5pt; margin-top: 2pt !important; }
  .demo a { color: var(--orange); }
  footer {
    margin-top: 20pt; padding-top: 8pt; border-top: 1pt solid #d8d8e2;
    color: var(--muted); font-size: 8.5pt;
  }
</style>
</head>
<body>
  <div class="masthead">
    <div>
      <p class="event">${esc(g.event)}</p>
      <h1>${esc(g.title)}</h1>
      <p class="sub">${esc(g.subtitle)}</p>
    </div>
    <div class="qr">
      <img src="qr-glossary.png" alt="QR code to the live glossary" />
      <p>Live version, on your phone<br/>${LIVE_URL.replace('https://', '')}</p>
    </div>
  </div>
  <p class="promise">No dumb questions. If you hear a word today that isn't on this page, ask — out loud or by pointing at your phone.</p>
  ${sectionsHtml}
  <footer>
    Prepared for GHX by Mind On Fire — Zack Proser, Nick Nisi, and Nick Cannariato ·
    Updated ${esc(g.updated)} · Live version with interactive demos: ${LIVE_URL}
  </footer>
</body>
</html>`

const htmlPath = path.join(outDir, 'glossary-print.html')
fs.writeFileSync(htmlPath, html)

// QR code: a committed asset — only fetch if missing, so exports work offline
// and don't clobber the checked-in high-res version.
const qrPath = path.join(outDir, 'qr-glossary.png')
if (!fs.existsSync(qrPath)) await fetchQRCode(LIVE_URL, qrPath)

execFileSync(CHROME, [
  '--headless',
  '--disable-gpu',
  '--no-pdf-header-footer',
  `--print-to-pdf=${path.join(outDir, 'glossary.pdf')}`,
  pathToFileURL(htmlPath).href,
])

const termCount = g.sections.reduce((n, s) => n + s.terms.length, 0)
console.log(`Exported ${termCount} terms across ${g.sections.length} levels to ${outDir}`)
console.log('  glossary.md, glossary-print.html, glossary.pdf')
